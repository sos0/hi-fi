function myUpdate(){
  self.triggerValue = Controller.getValue(Controller.Standard.RT);

  if(self.triggerValue > 0.6) {
      if(!self.isCasting) {
        self.isCasting = true;
        self.onCastingBegan();
      }

      self.onCastingUpdate();
  }
  else if(self.isCasting) {
    self.isCasting = false;
    print("ended");
    self.onCastingEnded();
  }
}

